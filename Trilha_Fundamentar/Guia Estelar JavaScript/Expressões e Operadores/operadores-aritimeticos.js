// Operadores Aritméticos

// multiplicação *
console.log(3 * 5)

// divisão /
console.log(3 / 5)

// soma +
console.log(3 + 5)

// subtração -
console.log(3 - 5)

// resto da divisão %
let remainder
remainder = 11 % 3
console.log(remainder)

// incremento ++
let increment = 0
increment ++
console.log(++increment)

// decremento --
let decrement = 0
console.log(decrement--)

// exponencial **
console.log(3 ** 3)